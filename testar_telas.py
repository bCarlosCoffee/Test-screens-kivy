from os import system

def testar(arquivo="main.py", screen="", portrait='portrait', scale=.65):

	system(
		f'python {arquivo} -m screen:{screen},{portrait},scale={scale}'
	)

test_all_screen = False
for screen in screens.keys():
	print(
		"""
		Screen to be used:

		Name: {name}
		Width: {width}
		Height: {height}
		DPI: {dpi}
		Density: {density}


		Commands:
			[ s ] = Skip to another screen.
			[ e ] = Exit.
			[ tall ] = No more question and test all screens.

			Anyone to continue.

		""".format(
			name=screens[screen]['Name'],
			width=screens[screen]['Width'],
			height=screens[screen]['Height'],
			dpi=screens[screen]['DPI'],
			density=screens[screen]['Density']
		)
	)


	if test_all_screen == False:
		choice = input('#~>  ').strip().lower()

		if choice == 'e':
			break

		if choice == 'tall':
			test_all_screen = True

		if choice == 's':
			continue


	try:
		testar(screen=screen)

	except KeyboardInterrupt:
		break